const fs = require("node:fs");
const path = require("node:path");
const { spawnSync } = require("node:child_process");

const repoRoot = path.resolve(__dirname, "..");
const outputFile = path.join(repoRoot, "packages", "steam-bridge", "src", "generated-steamworks-enums.ts");
const checkOnly = process.argv.includes("--check");

const generated = renderSteamworksEnums();

if (checkOnly) {
  const current = fs.existsSync(outputFile) ? normalizeLineEndings(fs.readFileSync(outputFile, "utf8")) : "";
  if (current !== generated) {
    throw new Error("Generated Steamworks enum constants are out of date. Run `npm run steamworks-enums:generate`.");
  }
} else {
  fs.writeFileSync(outputFile, generated);
}

function normalizeLineEndings(value) {
  return value.replace(/\r\n/g, "\n");
}

function renderSteamworksEnums() {
  const sdkRoot = findSteamworksSysRoot();
  const metadataFile = path.join(sdkRoot, "lib", "steam", "public", "steam", "steam_api.json");
  const metadata = JSON.parse(fs.readFileSync(metadataFile, "utf8"));
  const lines = [
    "/* This file is generated by scripts/generate-steamworks-enums.cjs. Do not edit by hand. */",
    "",
    "export const SteamworksEnums = {"
  ];

  for (const enumDefinition of metadata.enums ?? []) {
    lines.push(`  ${property(enumDefinition.enumname)}: {`);
    for (const value of enumDefinition.values ?? []) {
      lines.push(`    ${property(value.name)}: ${numberLiteral(value.value)},`);
    }
    lines.push("  },");
  }

  lines.push(
    "} as const;",
    "",
    "export type SteamworksEnumName = keyof typeof SteamworksEnums;",
    "export type SteamworksEnumValueName<T extends SteamworksEnumName> = keyof typeof SteamworksEnums[T];",
    "export type SteamworksEnumValue<T extends SteamworksEnumName> =",
    "  (typeof SteamworksEnums[T])[SteamworksEnumValueName<T>];",
    ""
  );

  return lines.join("\n");
}

function findSteamworksSysRoot() {
  const metadata = spawnSync("cargo", ["metadata", "--format-version", "1"], {
    cwd: repoRoot,
    encoding: "utf8",
    shell: false
  });

  if (metadata.error) {
    throw metadata.error;
  }
  if (metadata.status !== 0) {
    process.stderr.write(metadata.stderr || metadata.stdout);
    process.exit(metadata.status ?? 1);
  }

  const parsed = JSON.parse(metadata.stdout);
  const pkg = parsed.packages.find((candidate) => candidate.name === "steamworks-sys");
  if (!pkg) {
    throw new Error("cargo metadata did not include steamworks-sys.");
  }

  return path.dirname(pkg.manifest_path);
}

function property(name) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name) ? name : JSON.stringify(name);
}

function numberLiteral(value) {
  const normalized = String(value);
  if (!/^-?(?:0x[0-9A-Fa-f]+|\d+)$/.test(normalized)) {
    throw new Error(`Unsupported Steamworks enum value: ${value}`);
  }
  return normalized;
}
